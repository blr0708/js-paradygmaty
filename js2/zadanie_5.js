// Главная функция-фабрика, которая "производит" счетчики
function createCounter() {
  
  // 1. Создаем локальную переменную. 
  // Она "приватная" - снаружи к ней обратиться невозможно.
  let count = 0;
  
  // 2. Возвращаем (return) ВНУТРЕННЮЮ безымянную функцию.
  // В этот момент создается ЗАМЫКАНИЕ: эта внутренняя функция 
  // "запоминает" переменную count в своем рюкзаке.
  return function() {
    
    // При каждом вызове мы берем наш спрятанный count и увеличиваем на 1
    count++; 
    
    // Отдаем наружу результат
    return count; 
  };
}

// --- Проверка работы ---

// Вызываем createCounter() первый раз.
// Она создает переменную count = 0 и возвращает нам внутреннюю функцию.
// Мы кладем эту возвращенную функцию в константу counter1.
const counter1 = createCounter(); 

console.log(counter1()); // Выведет 1. Вспомнила count (0), сделала +1.
console.log(counter1()); // Выведет 2. Вспомнила count (1), сделала +1.
console.log(counter1()); // Выведет 3. Вспомнила count (2), сделала +1.

// Вызываем createCounter() ВТОРОЙ раз.
// Она создает АБСОЛЮТНО НОВУЮ, независимую переменную count = 0.
// Возвращает новую функцию, которую мы кладем в counter2.
const counter2 = createCounter(); 

console.log(counter2()); // Выведет 1. У counter2 свой собственный, отдельный "рюкзак".
console.log(counter2()); // Выведет 2.