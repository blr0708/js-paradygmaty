// 1. Функция генерации случайного числа от 0 do max
const random = (max) => {
  // Используем формулу: случайное число от 0 до max включительно
  return Math.floor(Math.random() * (max + 1));
};

// 2. Создаем пустой массив, куда будем складывать числа
const myArray = [];

// 3. Генерируем 20 чисел с помощью цикла
for (let i = 0; i < 20; i++) {
  // Генерируем число (например, от 0 до 100)
  const newNumber = random(100);
  // Добавляем его в конец нашего массива
  myArray.push(newNumber);
}

console.log("Массив до сортировки:", myArray);

// 4. Сортируем массив (используем правильную сортировку для чисел!)
myArray.sort((a, b) => a - b);
console.log("Массив после сортировки:", myArray);

// 5. Считаем сумму всех элементов
let sum = 0;
// Специальный цикл 'for...of' для массивов. 
// Он сам проходит по каждому элементу массива по очереди.
for (const num of myArray) {
  sum += num; // Прибавляем каждое число к общей сумме
}

// 6. Считаем среднее значение (сумма деленная на количество элементов)
const average = sum / myArray.length;

console.log("-------------------");
console.log(`Сумма всех чисел: ${sum}`);
console.log(`Среднее значение: ${average}`);